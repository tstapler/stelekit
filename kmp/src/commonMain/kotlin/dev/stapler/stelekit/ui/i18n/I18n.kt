class I18n(val language: Language) {
    fun t(key: String): String {
        return translations[language]?.get(key) ?: key
    }
}

val LocalI18n = staticCompositionLocalOf { I18n(Language.ENGLISH) }

@Composable
fun t(key: String): String {
    return LocalI18n.current.t(key)
}
